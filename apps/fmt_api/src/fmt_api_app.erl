-module(fmt_api_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
  leptus:start_listener(http, [{'_', [{fmt_handler, undefined_state}]}]),
  fmt_api_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

